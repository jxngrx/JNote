export const ALL_THEME_PRESETS: ThemePreset[] = [
  ...DARK_THEME_PRESETS,
  ...LIGHT_THEME_PRESETS,
];

export const THEME_PRESET_MAP = Object.fromEntries(
  ALL_THEME_PRESETS.map((preset) => [preset.id, preset])
) as Record<string, ThemePreset>;

export const DEFAULT_DARK_PRESET_ID = 'noterx-dark';
export const DEFAULT_LIGHT_PRESET_ID = 'noterx-light';

export function getPresetById(id: string): ThemePreset | undefined {
  return THEME_PRESET_MAP[id];
}

export function getPresetsByMode(mode: ThemeMode): ThemePreset[] {
  return mode === 'dark' ? DARK_THEME_PRESETS : LIGHT_THEME_PRESETS;
}
