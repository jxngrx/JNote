import type { ThemeColors, ThemeMode, ThemePreset } from '@/lib/theme-types';

function dark(
  id: string,
  name: string,
  colors: Omit<ThemeColors, 'borderOpacity' | 'borderStrongOpacity'> &
    Partial<Pick<ThemeColors, 'borderOpacity' | 'borderStrongOpacity'>>
): ThemePreset {
  return {
    id,
    name,
    mode: 'dark',
    colors: {
      borderOpacity: colors.borderOpacity ?? 0.06,
      borderStrongOpacity: colors.borderStrongOpacity ?? 0.1,
      ...colors,
    },
  };
}

function light(
  id: string,
  name: string,
  colors: Omit<ThemeColors, 'borderOpacity' | 'borderStrongOpacity'> &
    Partial<Pick<ThemeColors, 'borderOpacity' | 'borderStrongOpacity'>>
): ThemePreset {
  return {
    id,
    name,
    mode: 'light',
    colors: {
      borderOpacity: colors.borderOpacity ?? 0.08,
      borderStrongOpacity: colors.borderStrongOpacity ?? 0.14,
      ...colors,
    },
  };
}

