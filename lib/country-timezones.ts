export function getTimezoneForCountry(countryId: string): string {
  const multi = MULTI_COUNTRY_TIMEZONES[countryId];
  if (multi?.length) return multi[0].timezone;
  return COUNTRY_TIMEZONES[countryId] ?? 'UTC';
}

export function getTimezoneOptionsForCountry(countryId: string): TimezoneOption[] {
  const multi = MULTI_COUNTRY_TIMEZONES[countryId];
  if (multi?.length) return multi;
  const timezone = COUNTRY_TIMEZONES[countryId] ?? 'UTC';
  return [{ timezone, label: getShortTimezoneName(timezone) }];
}

export function getTimezoneOptionLabel(
  countryId: string,
  timezone: string
): string {
  const options = getTimezoneOptionsForCountry(countryId);
  return (
    options.find((o) => o.timezone === timezone)?.label ??
    getShortTimezoneName(timezone)
  );
}

export function formatClockTime(
  timezone: string,
  now = new Date(),
  showSeconds = true
) {
  return new Intl.DateTimeFormat('en-GB', {
    timeZone: timezone,
    hour: '2-digit',
    minute: '2-digit',
    ...(showSeconds ? { second: '2-digit' } : {}),
    hour12: false,
  }).format(now);
}

export function formatClockDate(timezone: string, now = new Date()) {
  return new Intl.DateTimeFormat('en-US', {
    timeZone: timezone,
    weekday: 'short',
    month: 'short',
    day: 'numeric',
  }).format(now);
}

export function getUtcOffsetLabel(timezone: string, now = new Date()) {
  const parts = new Intl.DateTimeFormat('en-US', {
    timeZone: timezone,
    timeZoneName: 'shortOffset',
  }).formatToParts(now);
  return parts.find((p) => p.type === 'timeZoneName')?.value ?? 'UTC';
}

export function getShortTimezoneName(timezone: string) {
  return timezone.split('/').pop()?.replace(/_/g, ' ') ?? timezone;
}

const DAY_DOT_COUNT = 240;
const YEAR_DOT_COLUMNS = 20;

export type FocusPeriod = 'day' | 'month' | 'year';
export type TimeIntervalMinutes = 1 | 10 | 30;

function getTimezoneYMD(timezone: string, now = new Date()) {
  const parts = new Intl.DateTimeFormat('en-US', {
    timeZone: timezone,
    year: 'numeric',
    month: 'numeric',
    day: 'numeric',
  }).formatToParts(now);

  return {
    year: Number(parts.find((p) => p.type === 'year')?.value ?? 0),
    month: Number(parts.find((p) => p.type === 'month')?.value ?? 1),
    day: Number(parts.find((p) => p.type === 'day')?.value ?? 1),
  };
}

function getDaysInMonth(year: number, month: number) {
  return new Date(year, month, 0).getDate();
}

function isLeapYear(year: number) {
  return (year % 4 === 0 && year % 100 !== 0) || year % 400 === 0;
}

function getDayOfYear(year: number, month: number, day: number) {
  let total = day;
  for (let m = 1; m < month; m++) {
    total += getDaysInMonth(year, m);
  }
  return total;
}

function getDayFraction(timezone: string, now: Date) {
  return getTimezoneDayProgress(timezone, now).secondsElapsed / 86400;
}

export function getTimezoneDayProgress(timezone: string, now = new Date()) {
  const parts = new Intl.DateTimeFormat('en-US', {
    timeZone: timezone,
    hour: 'numeric',
    minute: 'numeric',
    second: 'numeric',
    hour12: false,
  }).formatToParts(now);

  const hour = Number(parts.find((p) => p.type === 'hour')?.value ?? 0);
  const minute = Number(parts.find((p) => p.type === 'minute')?.value ?? 0);
  const second = Number(parts.find((p) => p.type === 'second')?.value ?? 0);
  const secondsElapsed = hour * 3600 + minute * 60 + second;
  const pct = (secondsElapsed / 86400) * 100;

  return {
    secondsElapsed,
    secondsRemaining: 86400 - secondsElapsed,
    minutesElapsed: Math.floor(secondsElapsed / 60),
    minutesRemaining: Math.floor((86400 - secondsElapsed) / 60),
    pct: Math.round(pct * 10) / 10,
    filledDots: Math.min(
      DAY_DOT_COUNT,
      Math.floor((secondsElapsed / 86400) * DAY_DOT_COUNT)
    ),
    dotCount: DAY_DOT_COUNT,
  };
}

export function formatFocusLongDate(timezone: string, now = new Date()) {
  return new Intl.DateTimeFormat('en-GB', {
    timeZone: timezone,
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  }).format(now);
}

export function formatFocusMonthYear(timezone: string, now = new Date()) {
  return new Intl.DateTimeFormat('en-GB', {
    timeZone: timezone,
    month: 'long',
    year: 'numeric',
  }).format(now);
}

export function getTimezoneMonthProgress(timezone: string, now = new Date()) {
  const { year, month, day } = getTimezoneYMD(timezone, now);
  const daysInMonth = getDaysInMonth(year, month);
  const elapsed = day - 1 + getDayFraction(timezone, now);
  const pct = (elapsed / daysInMonth) * 100;

  return {
    elapsed: Math.floor(elapsed),
    remaining: daysInMonth - Math.ceil(elapsed),
    pct: Math.round(pct * 10) / 10,
    filledDots: Math.min(daysInMonth, Math.floor(elapsed)),
    dotCount: daysInMonth,
    dotColumns: 7,
    subtitle: `Each dot is one day · ${formatFocusMonthYear(timezone, now)}`,
    footer: formatFocusMonthYear(timezone, now),
    statLabels: ['Days elapsed', 'Days remaining', 'Month complete'] as const,
  };
}

export function getTimezoneYearProgress(timezone: string, now = new Date()) {
  const { year, month, day } = getTimezoneYMD(timezone, now);
  const daysInYear = isLeapYear(year) ? 366 : 365;
  const dayOfYear = getDayOfYear(year, month, day);
  const elapsed = dayOfYear - 1 + getDayFraction(timezone, now);
  const pct = (elapsed / daysInYear) * 100;

  return {
    elapsed: Math.floor(elapsed),
    remaining: daysInYear - Math.ceil(elapsed),
    pct: Math.round(pct * 10) / 10,
    filledDots: Math.min(daysInYear, Math.floor(elapsed)),
    dotCount: daysInYear,
    dotColumns: YEAR_DOT_COLUMNS,
    subtitle: `Each dot is one day · ${year}`,
    footer: String(year),
    statLabels: ['Days elapsed', 'Days remaining', 'Year complete'] as const,
  };
}

const TIME_INTERVAL_CONFIG: Record<
  TimeIntervalMinutes,
  { dotColumns: number; label: string }
> = {
  1: { dotColumns: 48, label: '1 minute' },
  10: { dotColumns: 12, label: '10 minutes' },
  30: { dotColumns: 12, label: '30 minutes' },
};

export function getTimezoneDayProgressView(
  timezone: string,
  now = new Date(),
  intervalMinutes: TimeIntervalMinutes = 10
) {
  const day = getTimezoneDayProgress(timezone, now);
  const offset = getUtcOffsetLabel(timezone, now);
  const slotSeconds = intervalMinutes * 60;
  const dotCount = 1440 / intervalMinutes;
  const completedSlots = Math.floor(day.secondsElapsed / slotSeconds);
  const secondsIntoSlot = day.secondsElapsed % slotSeconds;
  const activeDotFraction = secondsIntoSlot / slotSeconds;
  const hasActiveDot = completedSlots < dotCount;

  return {
    elapsed: day.minutesElapsed,
    remaining: day.minutesRemaining,
    pct: day.pct,
    completedDots: completedSlots,
    activeDotFraction,
    hasActiveDot,
    dotCount,
    dotColumns: TIME_INTERVAL_CONFIG[intervalMinutes].dotColumns,
    subtitle: `Each dot is ${TIME_INTERVAL_CONFIG[intervalMinutes].label} · ${offset}`,
    footer: `Today is ${formatFocusLongDate(timezone, now)}`,
    statLabels: ['Minutes elapsed', 'Minutes remaining', 'Day complete'] as const,
  };
}

